n1 = input('Digite um valor:')
print('O tipo primtivo desse valor é', type(n1))
print('Só tem espaço?', n1.isspace())
print('Só tem número?', n1.isalnum())
print('Só tem letra?', n1.isalpha())
print('Está em maiusculas?', n1.isupper())
print('É alfanumérico?', n1.isalnum())
print('Está em minusculas?', n1.islower())

